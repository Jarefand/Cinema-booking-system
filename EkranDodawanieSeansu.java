//Dodanie listy filmów do jComboBox
    private void addItemToCombobox(){
        //Czyszczenie Combobox
        jComboBox1.removeAllItems();
        
        Set<Map.Entry<Integer, Film>> entries = mainGUI.kino.mapa_filmow.entrySet();
        Iterator<Map.Entry<Integer, Film>> filmyIterator = entries.iterator();
       
        //Odczyt kolejnych danych z LinkedHashMapy i umieszczenie ich w wierszach tabeli
        while(filmyIterator.hasNext()) {
            Map.Entry<Integer, Film> entry = filmyIterator.next();
            jComboBox1.addItem(entry.getKey() + ". " + entry.getValue().getName());
            keyFilmList.add(entry.getKey());
       }
    
        
    }
        
    public void activate(){
        this.addItemToCombobox();
        this.jComboBoxGodz.setSelectedIndex(0);
        this.jComboBoxSala.setSelectedIndex(0);
        this.jComboBox1.setSelectedIndex(0);
        Float value = new Float(0.0);
        this.jSpinnerCena.setValue(value);
        Date curDate = new Date();
        this.jDateChooser2.setDate(curDate);
        
        this.setVisible(true);
        
    }

