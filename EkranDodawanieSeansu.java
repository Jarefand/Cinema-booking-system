    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed

        int response = JOptionPane.showConfirmDialog(this, "Czy chcesz dodać seans o podanych paramentrach?", "Potwierdź", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if(response == JOptionPane.YES_OPTION){
            //Pobranie danych z daty i godziny
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");
            String kal = dateFormat.format(jDateChooser2.getDate());
            String godz = (String) jComboBoxGodz.getSelectedItem();
            String dat = kal + " " + godz;

            //pobranie klucza filmu
            int key = keyFilmList.get(jComboBox1.getSelectedIndex()) + 1;
            //pobranie sali
            int sala =  jComboBoxSala.getSelectedIndex();
            //pobranie ceny
            float cena = (float) jSpinnerCena.getValue();


            try {
                mainGUI.kino.dodajSeans(key, dat, sala, cena);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(EkranDodawanieSeansu.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            JOptionPane.showMessageDialog(this, "Seans został dodany prawidłowo");
            this.setVisible(false);
            this.mainGUI.activate();
        }
        
    }//GEN-LAST:event_jButtonAddActionPerformed

